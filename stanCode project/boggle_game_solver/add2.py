import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    cur1 = l1
    lst1 = []
    while cur1 is not None:
        lst1.append(cur1.val)
        cur1 = cur1.next
    total1 = 0
    for i in range(len(lst1)):
        total1 += lst1[i]*(10**i)

    cur2 = l2
    lst2 = []
    while cur2 is not None:
        lst2.append(cur2.val)
        cur2 = cur2.next
    total2 = 0
    for i in range(len(lst2)):
        total2 += lst2[i] * (10 ** i)

    total = total1 + total2
    if total == 0:
        ans = ListNode(0, None)
    else:
        ans = None
        while total/10 >= 0.1:
            new_node = ListNode(total % 10, None)
            total //= 10
            if ans is None:
                ans = new_node
            else:
                cur = ans
                while cur.next is not None:
                    cur = cur.next
                cur.next = new_node
    return ans


